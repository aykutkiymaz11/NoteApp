import androidx.navigation.navArgument
import com.example.noteapp.feature_note.presentation.add_edit_note.views.AddEditNoteScreen
import com.example.noteapp.feature_note.presentation.notes.views.NotesScreen
import com.example.noteapp.feature_note.presentation.util.Screen
import com.example.noteapp.ui.theme.NoteAppTheme
import dagger.hilt.android.AndroidEntryPoint

@AndroidEntryPoint
class MainActivity : ComponentActivity() {
    @OptIn(ExperimentalMaterial3Api::class)
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        enableEdgeToEdge()
        setContent {
            NoteAppTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colors.background
                ) {
                    val navController = rememberNavController()
                    NavHost(
                        navController = navController,
                        startDestination = Screen.NotesScreen.route
                    ) {
                        composable(route = Screen.NotesScreen.route) {
                            NotesScreen(navController = navController)
                        }
                        composable(
                            route = Screen.AddEditNoteScreen.route + "?noteId={noteId}",
                            arguments = listOf(navArgument("noteId") {
                                type = NavType.IntType
                                defaultValue = -1  // Eğer parametre gelmezse, varsayılan olarak -1 alır
                            })
                        ) { backStackEntry ->
                            val noteId = backStackEntry.arguments?.getInt("noteId")
                            AddEditNoteScreen(navController = navController, noteId = noteId.toString())
                        }



                    }
                }
            }
        }
    }
}

